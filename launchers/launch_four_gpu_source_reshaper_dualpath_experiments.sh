#!/bin/bash

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
LOG_DIR="${LOG_DIR:-$SCRIPT_DIR/../logs/source_reshaper_dualpath_$(date +%Y%m%d_%H%M%S)}"
mkdir -p "$LOG_DIR"

RESHAPER_A="${RESHAPER_A:-residual_temporal_conv}"
STRENGTH_A="${STRENGTH_A:-0.10}"
KERNEL_A="${KERNEL_A:-3}"
REG_A="${REG_A:-0.05}"
DUAL_CLS_A="${DUAL_CLS_A:-1.00}"
DUAL_REL_A="${DUAL_REL_A:-0.05}"
TAG_A="${TAG_A:-${RESHAPER_A}_s${STRENGTH_A}_k${KERNEL_A}_r${REG_A}_dual_c${DUAL_CLS_A}_rel${DUAL_REL_A}}"

RESHAPER_B="${RESHAPER_B:-residual_temporal_conv}"
STRENGTH_B="${STRENGTH_B:-0.15}"
KERNEL_B="${KERNEL_B:-3}"
REG_B="${REG_B:-0.05}"
DUAL_CLS_B="${DUAL_CLS_B:-1.00}"
DUAL_REL_B="${DUAL_REL_B:-0.10}"
TAG_B="${TAG_B:-${RESHAPER_B}_s${STRENGTH_B}_k${KERNEL_B}_r${REG_B}_dual_c${DUAL_CLS_B}_rel${DUAL_REL_B}}"

CUDA_VISIBLE_DEVICES=0 \
  SOURCE_FEATURE_RESHAPER="$RESHAPER_A" SOURCE_FEATURE_RESHAPER_STRENGTH="$STRENGTH_A" SOURCE_FEATURE_RESHAPER_KERNEL_SIZE="$KERNEL_A" SOURCE_FEATURE_RESHAPER_REG_TRADE_OFF="$REG_A" SOURCE_FEATURE_DUAL_CLS_TRADE_OFF="$DUAL_CLS_A" SOURCE_FEATURE_DUAL_RELATION_TRADE_OFF="$DUAL_REL_A" RESHAPER_TAG="$TAG_A" \
  PARTITION=A \
  bash "$SCRIPT_DIR/ideas/run_timematch_closed_set_sourcephasecompact_reshaper_dualpath_partition.sh" \
  > "$LOG_DIR/gpu0_${TAG_A}_partition_A.log" 2>&1 &

CUDA_VISIBLE_DEVICES=1 \
  SOURCE_FEATURE_RESHAPER="$RESHAPER_A" SOURCE_FEATURE_RESHAPER_STRENGTH="$STRENGTH_A" SOURCE_FEATURE_RESHAPER_KERNEL_SIZE="$KERNEL_A" SOURCE_FEATURE_RESHAPER_REG_TRADE_OFF="$REG_A" SOURCE_FEATURE_DUAL_CLS_TRADE_OFF="$DUAL_CLS_A" SOURCE_FEATURE_DUAL_RELATION_TRADE_OFF="$DUAL_REL_A" RESHAPER_TAG="$TAG_A" \
  PARTITION=B \
  bash "$SCRIPT_DIR/ideas/run_timematch_closed_set_sourcephasecompact_reshaper_dualpath_partition.sh" \
  > "$LOG_DIR/gpu1_${TAG_A}_partition_B.log" 2>&1 &

CUDA_VISIBLE_DEVICES=2 \
  SOURCE_FEATURE_RESHAPER="$RESHAPER_B" SOURCE_FEATURE_RESHAPER_STRENGTH="$STRENGTH_B" SOURCE_FEATURE_RESHAPER_KERNEL_SIZE="$KERNEL_B" SOURCE_FEATURE_RESHAPER_REG_TRADE_OFF="$REG_B" SOURCE_FEATURE_DUAL_CLS_TRADE_OFF="$DUAL_CLS_B" SOURCE_FEATURE_DUAL_RELATION_TRADE_OFF="$DUAL_REL_B" RESHAPER_TAG="$TAG_B" \
  PARTITION=A \
  bash "$SCRIPT_DIR/ideas/run_timematch_closed_set_sourcephasecompact_reshaper_dualpath_partition.sh" \
  > "$LOG_DIR/gpu2_${TAG_B}_partition_A.log" 2>&1 &

CUDA_VISIBLE_DEVICES=3 \
  SOURCE_FEATURE_RESHAPER="$RESHAPER_B" SOURCE_FEATURE_RESHAPER_STRENGTH="$STRENGTH_B" SOURCE_FEATURE_RESHAPER_KERNEL_SIZE="$KERNEL_B" SOURCE_FEATURE_RESHAPER_REG_TRADE_OFF="$REG_B" SOURCE_FEATURE_DUAL_CLS_TRADE_OFF="$DUAL_CLS_B" SOURCE_FEATURE_DUAL_RELATION_TRADE_OFF="$DUAL_REL_B" RESHAPER_TAG="$TAG_B" \
  PARTITION=B \
  bash "$SCRIPT_DIR/ideas/run_timematch_closed_set_sourcephasecompact_reshaper_dualpath_partition.sh" \
  > "$LOG_DIR/gpu3_${TAG_B}_partition_B.log" 2>&1 &

wait
